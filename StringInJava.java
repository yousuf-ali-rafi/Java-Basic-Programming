public class StringInJava
{
    public static void main(String[] args)
    {
        String name = new String("Yousuf Ali");

        System.out.println(name + " Rafi");
        System.out.println(name.charAt(1));   // printing index value of string
        System.out.println(name.concat(" , Age - 27"));  // Concat
    }
}
